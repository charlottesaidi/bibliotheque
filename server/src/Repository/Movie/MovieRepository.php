<?php

namespace App\Repository\Movie;

use App\Entity\Movie\Movie;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Movie>
 *
 * @method Movie|null find($id, $lockMode = null, $lockVersion = null)
 * @method Movie|null findOneBy(array $criteria, array $orderBy = null)
 * @method Movie[]    findAll()
 * @method Movie[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class MovieRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Movie::class);
    }

    public function save(Movie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Movie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    /**
     * @return Movie[] Returns an array of every Movie objects found in database
     */
    public function getAll(): array {
        return $this->createQueryBuilder('m')
            ->select('m, f')
            ->innerJoin('m.file', 'f')
            ->orderBy('m.createdAt', 'DESC')
            ->getQuery()->getArrayResult();
    }

    /**
     * @return Movie[] Returns an array of latest Movie objects found in database
     */
    public function getLastUploadedMovies(int $limit): array {
        return $this->createQueryBuilder('m')
            ->select('m, f')
            ->innerJoin('m.file', 'f')
            ->orderBy('m.updatedAt', 'DESC')
            ->setMaxResults($limit)
            ->getQuery()->getArrayResult();
    }

